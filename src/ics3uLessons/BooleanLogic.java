package ics3uLessons;

public class BooleanLogic {

	public static void main(String[] args) {

		// boolean logical operators:
		
		// && - Logical "AND"
		if ((2 > 1) && (5 > 3))  {
			
			System.out.println("AND block executed.");
		}
		
		// || - Logical "OR"
		if ((2 > 1) || (2 < 1)) {
			
			System.out.println("OR block executed.");
		}
	}
}
