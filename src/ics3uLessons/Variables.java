package ics3uLessons;

public class Variables {

	public static void main(String[] args) {

		// double slash is a comment
		// this is also a comment
		
		/* this is a comment
		  this is continuation of the comment
		 */
		
		// variables follow camelCase convention
		
		// variables
		int myFirstVariable = 0;
		double myDouble = 0.0;
		char myChar = 'a';
		String myString = "";
		boolean myBoolean1 = true;
		boolean myBoolean2 = false;
		
		int my_integer = 0;  // another convention
		
		// constant variable uses keyword "final" and follows constant variable
		// naming convention
		final double TAX_RATE = 0.13;
		

	}

}
