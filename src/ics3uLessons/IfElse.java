package ics3uLessons;

public class IfElse {

	public static void main(String[] args) {
		
		// Comparison Operators: (used for numbers and single characters)
		// >, >=, <, <=, ==, !=
		
		if (13 > 17)  {
			
			System.out.println("True!");
		}

		else  {
			
			// this block of code will execute if false
			System.out.println("False!");
		}
	}

}
