package lessons;

public class WrapperClasses {

	public static void main(String[] args) {

		// Wrapper classes are used to store primitives
		// all primitives have an associated wrapper class
		int myInt1 = 13;
		Integer myInt2 = 13;

		double myDouble1 = 13.3;
		Double myDouble2 = 13.3;
		
		boolean myBoolean1 = true;
		Boolean myBoolean2 = true;
		
	}

}
